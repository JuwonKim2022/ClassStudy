package sec06.exam03;

public class OperPromotionEx1 {

	public static void main(String[] args) {
		
		byte byteVal1 = 10;
		byte byteVal2 = 20;
		int intVal1 = byteVal1 + byteVal2;
		System.out.println(intVal1);
		
		char charVal1 = 'A';
		char charVal2 = 1;
		int intVal2 = charVal1 + charVal2;
		System.out.println(intVal2);
		System.out.println((char)intVal2);
		
		int intVal3 = 10;
		int intVal4 = intVal3 / 4;
		System.out.println(intVal4);
		
		double doubleVal4 = intVal3 / 4.0;
		System.out.println(doubleVal4);
		
		int x = 1;
		int y = 2;
		double result = (double)x/y;
		System.out.println(result);

	}

}
