package sec06.exam02;

public class ByteOperEx1 {

	public static void main(String[] args) {
		
		byte result1 = 10 + 30;
		System.out.println(result1);
		System.out.println();
		byte x = 10;
		byte y = 20;
		int result2 = x + y;
		System.out.println(result2);
		System.out.println();
		byte x2 = 30;
		long y2 = 40;
		long result3 = x2 + y2;
		System.out.println(result3);
		

	}

}
